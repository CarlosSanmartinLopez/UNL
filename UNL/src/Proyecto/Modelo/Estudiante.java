package Proyecto.Modelo;

/**
 * Clase que define la estrutura de un Estudiante
 * @author Grupo1
 */
public class Estudiante {
    private String codigo, nombres, apellidos, cedula, paralelo, carrera;    
    
    public Estudiante() {
        codigo = "";
        nombres = "";
        apellidos = "";
        cedula = "";
        paralelo = "";
        carrera = "";
    }
    
    public Estudiante(String codigo, String nombres, String apellidos, String cedula, String paralelo, String carrera) {
        this.codigo = codigo;
        this.nombres = nombres;
        this.apellidos = apellidos;
        this.cedula = cedula;
        this.paralelo = paralelo;
        this.carrera = carrera;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getParalelo() {
        return paralelo;
    }

    public void setParalelo(String paralelo) {
        this.paralelo = paralelo;
    }

    public String getCarrera() {
        return carrera;
    }

    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }
}
